package com.example.kharrison.my_application


class CalcFunctions {

   fun add(leftNum : Int, rightNum  : Int) = leftNum + rightNum
   fun subtract(leftNum : Int, rightNum : Int) = leftNum - rightNum
   fun multiply(leftNum : Int, rightNum : Int) = leftNum * rightNum
   fun divide(leftNum : Int, rightNum : Int) = leftNum / rightNum

}